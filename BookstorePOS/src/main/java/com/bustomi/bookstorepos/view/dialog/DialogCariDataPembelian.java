    private void buttonMin1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonMin1ActionPerformed
        String nama=textFieldXNama.getText();
        
        if (!jRadioButton1.isSelected() && !jRadioButton2.isSelected()) {
            JOptionPane.showMessageDialog(this, "Silahkah pilih jenis Barang");
        }else if (nama.trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Masukan nama pencarian");
        }else {
            List<Item> list=new ArrayList<>();
            if (jRadioButton1.isSelected()) {
                BukuService bukuService=SpringManager.getInstance().getBean(BukuService.class);
                List<Buku> bukus=bukuService.findAll(nama);
                for (Buku buku : bukus) {
                    list.add(buku.getItem());
                }
            }else if(jRadioButton2.isSelected()){
                BarangService barangService=SpringManager.getInstance().getBean(BarangService.class);
                List<Barang> barangs=barangService.findAll(nama);
                for (Barang barang : barangs) {
                    list.add(barang.getItem());
                }
            }            
            modelItem.load(list);
        }
        
        
    }//GEN-LAST:event_buttonMin1ActionPerformed

    private void buttonYellow1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonYellow1ActionPerformed
        Integer jmlh=(Integer) textFormatXJmlh.getValue();
        if (jTable1.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Barang pembelian belum dipilih");
        }else {
            int pilih=jTable1.convertRowIndexToModel(jTable1.getSelectedRow());            
            if (beli) {
                item=modelItem.ambilData(pilih);
                dispose();
            } else {
                if(jmlh < 1){
                    JOptionPane.showMessageDialog(this, "Jumlah pembelian minimal 1");
                } else{
                    detailPembelian = new DetailPembelian();                    
                    detailPembelian.setHarga(modelItem.ambilData(pilih).getHarga_beli());
                    detailPembelian.setJumlah(jmlh);
                    detailPembelian.setItem(modelItem.ambilData(pilih));
                    dispose();
                }
            }
            
        }
    }//GEN-LAST:event_buttonYellow1ActionPerformed

