    private void initDefaultValue() {
        
        ItemService service=SpringManager.getInstance().getBean(ItemService.class);                
        
        rowsPerPage = Integer.valueOf(jComboBoxPage.getSelectedItem().toString());
        totalRows = service.countRows();
        Double dblTotPage = Math.ceil(totalRows.doubleValue()/rowsPerPage.doubleValue());
        totalPage = dblTotPage.intValue();
        if (pageNumber == 1) {
            btnFirst.setEnabled(false);
            btnPrevious.setEnabled(false);
        } else {
            btnFirst.setEnabled(true);
            btnPrevious.setEnabled(true);
        }

        if (pageNumber.equals(totalPage)) {
            btnNext.setEnabled(false);
            btnLast.setEnabled(false);
        } else {
            btnNext.setEnabled(true);
            btnLast.setEnabled(true);
        }

        txtPageNumber.setText(String.valueOf(pageNumber));
        lblPageOf.setText(" of " + totalPage + " ");
        lblTotalRecord.setText("Total Record " + totalRows + " rows.");

        List<Item> list = service.findAll(pageNumber, rowsPerPage);
        modelBarcode.load(list);
    }
    
    private void cari(){
        String nama=textFieldXNama.getText();
        ItemService  service=SpringManager.getInstance().getBean(ItemService.class);
        Item item=service.find(nama);
        if (item == null) {
            JOptionPane.showMessageDialog(this, "Barang dengan kode "+nama+" tidak ditemukan");
        }else{
            modelBarcode.tambahsatu(item);
        }
    }
