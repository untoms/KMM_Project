    private void buttonYellow1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonYellow1ActionPerformed
        
        if (modelBarcode.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Data kosong");   
            return;
        }
        
        List<Item> list=new ArrayList<>();
        
        for (Item item1 : modelBarcode.getList()) {
            if (item1.getPilih() != null) {
                if (item1.getPilih() ) {
                    list.add(item1);                                        
                }                
            }
        }
        
        if(list.isEmpty()){
            JOptionPane.showMessageDialog(this, "Tidak ada data yang dipilih ");  
        }else{  
            
            List<Item> itemSource=new ArrayList<>();
            
            for (Item item : list) {
                if (item.getJmlhCetak() != null){
                    if (item.getJmlhCetak() > 0) {
                        for (int i = 0; i < item.getJmlhCetak(); i++) {
                            itemSource.add(item);
                        }
                    }                    
                }                
            }
            
            if (itemSource.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Jumlah cetak tidak valid");
                return;
            }
            
            try {
                InputStream inputStream=getClass().getResourceAsStream("/com/bustomi/d3ti/uns/report/cetakbarcode.jasper");

                JRDataSource dataSource=new JRBeanCollectionDataSource(itemSource, false);
                Map<String, Object> map=new HashMap<>();
                map.put(JRParameter.REPORT_DATA_SOURCE, dataSource);
                map.put(JRParameter.REPORT_LOCALE, new Locale("in", "ID"));
                map.put("TOKO", "UNSBookstore");

                JasperPrint jasperPrint=JasperFillManager.fillReport(inputStream, map);
                DialogLaporan dialogLaporan=new DialogLaporan(jasperPrint);
                dialogLaporan.setLocationRelativeTo(null);
                dialogLaporan.setVisible(true);
            } catch (JRException ex) {
                Logger.getLogger(DialogCetakBarcode.class.getName()).log(Level.SEVERE, null, ex);
            }            
        }
    }//GEN-LAST:event_buttonYellow1ActionPerformed

